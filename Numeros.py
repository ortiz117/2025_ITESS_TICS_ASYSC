uno = 3
dos = 11

#suma
suma = uno + dos
print("suma")
print (uno," + ",dos," = ",suma)
#multiplicacion
multi = uno * dos
print("multiplicacion")
print (uno," * ",dos," = ",multi)
#division
divi = uno / dos
print("division")
print (uno," / ",dos," = ",divi)
#division que retorna solo enteros
divi = uno // dos
print("division")
print (uno," // ",dos," = ",divi)
#resta
resta = uno - dos
print("resta")
print (uno," - ",dos," = ",resta)
# los paréntesis (()) pueden ser usados para agrupar
Agrupada = (50 - 5*6) / 4
print (Agrupada)

Resto = 17 % 3  # el operado % retorna el resto de la división
print (Resto)

potencia5 = 5 ** 2  # 5 al cuadrado
potencia7 = 2 ** 7  # 2 a la potencia de 7
print(potencia5)
print(potencia7)